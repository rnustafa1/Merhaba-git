package com.vektorel.java23.mustafa.ders5.ornek2;

public class BenzinIstasyonu {
	String adý;
	String adres;
	String marka;
	String tel;
	public String getAdý() {
		return adý;
	}
	public void setAdý(String adý) {
		this.adý = adý;
	}
	public String getAdres() {
		return adres;
	}
	public void setAdres(String adres) {
		this.adres = adres;
	}
	public String getMarka() {
		return marka;
	}
	public void setMarka(String marka) {
		this.marka = marka;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
}
